  <script src="https://code.jquery.com/jquery-3.6.0.min.js"></script>
  <script src="https://cdn.jsdelivr.net/npm/bootstrap@4.6.2/dist/js/bootstrap.bundle.min.js"></script>
  <script src="https://cdnjs.cloudflare.com/ajax/libs/aos/2.3.4/aos.js"></script>
  
  <script>
    // Initialize AOS
    AOS.init({ 
      duration: 800, 
      once: true,
      offset: 100 
    });
    
    // Remove loading spinner when page is loaded
    $(window).on('load', function() {
      $('#loading').fadeOut('slow');
    });

    // Contact Form Handling
    $(document).ready(function(){
      $('#contactForm').on('submit', function(e){
        e.preventDefault();
        
        const form = $(this);
        const submitBtn = form.find('button[type="submit"]');
        const spinner = $('#spinner');
        const submitText = $('#submitText');
        const alertDiv = $('#formAlert');
        
        // Basic validation
        if (!form[0].checkValidity()) {
          e.stopPropagation();
          form.addClass('was-validated');
          return;
        }
        
        // Show loading state
        submitText.text('Sending...');
        spinner.removeClass('d-none');
        submitBtn.prop('disabled', true);

        $.ajax({
          url: 'php/contact-process.php',
          type: 'POST',
          data: form.serialize(),
          success: function(response){
            alertDiv.html(`
              <div class="alert alert-success alert-dismissible fade show" role="alert">
                <i class="fas fa-check-circle mr-2"></i>
                <strong>Thank you!</strong> ${response} We'll get back to you within 2 hours.
                <button type="button" class="close" data-dismiss="alert">
                  <span>&times;</span>
                </button>
              </div>
            `);
            form.removeClass('was-validated')[0].reset();
          },
          error: function(xhr){
            const msg = xhr.responseText || 'Something went wrong. Please try again later.';
            alertDiv.html(`
              <div class="alert alert-danger alert-dismissible fade show" role="alert">
                <i class="fas fa-exclamation-circle mr-2"></i>
                <strong>Sorry,</strong> ${msg}
                <button type="button" class="close" data-dismiss="alert">
                  <span>&times;</span>
                </button>
              </div>
            `);
          },
          complete: function(){
            submitText.text('Send Message');
            spinner.addClass('d-none');
            submitBtn.prop('disabled', false);

            $('html, body').animate({
              scrollTop: alertDiv.offset().top - 100
            }, 500);
          }
        });
      });
      
      // Phone number formatting
      $('#phone').on('input', function(){
        let value = $(this).val().replace(/\D/g, '');
        if (value.startsWith('0')) {
          value = '+27' + value.substring(1);
        }
        // Format: +27 12 345 6789
        if (value.length > 2) {
          value = value.substring(0, 3) + ' ' + value.substring(3);
        }
        if (value.length > 6) {
          value = value.substring(0, 6) + ' ' + value.substring(6);
        }
        if (value.length > 10) {
          value = value.substring(0, 10) + ' ' + value.substring(10, 13);
        }
        $(this).val(value);
      });
    });
  </script>
